// Note - Arrays are mutable; we can change them whenever we want! *(UNLIKE Strings - strings are immutable).

//Example 1 -- This method is best for modifying/change a value that is already in a array (using index to change a value) - but there are many more methods to use

let shoppingList = [ 'oatmeal', 'bananas', 'spinach' ];
//"oatmeal", "bananas", "spinach"

//to modify
shoppingList[1] = 'grapes';
shoppingList[2] = 'kale';
shoppingList[3] = 'soy milk';
//"oatmeal", "grapes", "kale","soy milk"
